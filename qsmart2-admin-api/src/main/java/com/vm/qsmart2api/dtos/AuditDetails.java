package com.vm.qsmart2api.dtos;

import java.util.Date;

/**
 *
 * @author ASHOK
 */
public class AuditDetails {
    
    private Long id;
    private Long userId;
    private String action;
    private String status;
    private Date createdOn;
    private String failurReason;

    /**
     * @return the id
     */
    public Long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Long id) {
        this.id = id;
    }

    /**
     * @return the userId
     */
    public Long getUserId() {
        return userId;
    }

    /**
     * @param userId the userId to set
     */
    public void setUserId(Long userId) {
        this.userId = userId;
    }

    /**
     * @return the action
     */
    public String getAction() {
        return action;
    }

    /**
     * @param action the action to set
     */
    public void setAction(String action) {
        this.action = action;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

    /**
     * @return the createdOn
     */
    public Date getCreatedOn() {
        return createdOn;
    }

    /**
     * @param createdOn the createdOn to set
     */
    public void setCreatedOn(Date createdOn) {
        this.createdOn = createdOn;
    }

    /**
     * @return the failurReason
     */
    public String getFailurReason() {
        return failurReason;
    }

    /**
     * @param failurReason the failurReason to set
     */
    public void setFailurReason(String failurReason) {
        this.failurReason = failurReason;
    }
    
}
